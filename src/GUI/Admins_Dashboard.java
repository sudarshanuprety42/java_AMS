    public boolean isValidInput() {
        clearWarnings();
        boolean result = true;
        if (firstNametxt.getText().isEmpty()) {
            lbl.setText("*Enter Firstname");
            lbl.setVisible(true);
            result = false;
        } else if (!isAlphabet(firstNametxt.getText())) {
            lbl.setText("Invalid Firstname!");
            lbl.setVisible(true);
            result = false;
        }
        if (lastNametxt.getText().isEmpty()) {
            lbl1.setText("*Enter Lastname");
            lbl1.setVisible(true);
            result = false;
        } else if (!isAlphabet(lastNametxt.getText())) {
            lbl1.setText("Invalid Lastname!");
            lbl1.setVisible(true);
            result = false;
        }
        if (genderGroup.isSelected(null)) {
            lbl2.setVisible(true);
            result = false;
        }
        if (addresstxt.getText().isEmpty()) {
            lbl3.setText("*Enter address");
            lbl3.setVisible(true);
            result = false;
        } else if (!isAlphabet(addresstxt.getText())) {
            lbl3.setText("Invalid Address!");
            lbl3.setVisible(true);
            result = false;
        }
        if (citytxt.getText().isEmpty()) {
            lbl4.setText("*Enter City");
            lbl4.setVisible(true);
            result = false;
        } else if (!isAlphabet(citytxt.getText())) {
            lbl4.setText("Invalid City!");
            lbl4.setVisible(true);
            result = false;
        }
        if (phonetxt.getText().isEmpty()) {
            lbl5.setText("*Enter Phone");
            lbl5.setVisible(true);
            result = false;
        } else if (!isNumeric(phonetxt.getText())) {
            lbl5.setText("Invalid Phone!");
            lbl5.setVisible(true);
            result = false;
        }
        if (emailtxt.getText().isEmpty()) {
            lbl6.setText("*Enter Email");
            lbl6.setVisible(true);
            result = false;
        } else if (!isValidEmailAddress(emailtxt.getText())) {
            lbl6.setText("Invalid Email!");
            lbl6.setVisible(true);
            result = false;
        }
        if (dob.getDate() == null) {
            lbl7.setVisible(true);
            result = false;
        } else if (dob.getDate().compareTo(new Date()) > 0) {
            lbl7.setText("Invalid Date!");
            lbl7.setVisible(true);
            result = false;
        }

        if (passwordtxt.getText().isEmpty()) {
            lbl9.setVisible(true);
            result = false;
        }
        if (securitytxt.getText().isEmpty()) {
            lbl10.setVisible(true);
            result = false;
        }
        return result;
    }

